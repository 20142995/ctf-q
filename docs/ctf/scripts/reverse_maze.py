class Solution:

    def exist(self, board: List[List], target: [int, int]) -> bool:
        x, y = target
        row = len(board)
        col = len(board[0])
        lst = []

        def search(i, j, cur):
            if i >= row or i < 0:
                return False
            if j >= col or j < 0:
                return False
            letter = board[i][j]

            if letter == 1 or letter == None:
                return False
            if i == x and j == y:  # 找到最后一个
                lst.append([i,j])
                print(lst)
                f.extend(lst)
                return True

            lst.append([i, j])
            board[i][j] = None

            ret = search(i + 1, j, cur + 1) or \
                  search(i - 1, j, cur + 1) or \
                  search(i, j + 1, cur + 1) or \
                  search(i, j - 1, cur + 1)
            # 取消标记 回撤一步
            board[i][j] = letter
            lst.pop()
            return ret

        for i in range(row):
            for j in range(col):
                ret = search(i, j, 0)
                if ret:
                    return True
        print(lst)
        return False



def get_direction(board: List[List[int]]):
    res = ''
    for i in range(len(board) - 1):
        a, b = board[i], board[i + 1]
        r1, c1 = a
        r2, c2 = b
        if r2 - r1 == 1:
            res += 's'
        elif r2 - r1 == -1:
            res += 'w'
        elif c2 - c1 == 1:
            res += 'd'
        elif c2 - c1 == -1:
            res += 'a'
        else:
            print('error', i)
    return res

