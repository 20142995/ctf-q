from typing import List
UNMOVABLE = 0  # 墙为0
# 1.传start或实现  find_start(maze):
# 2.传target或实现  find_end(maze):
# 3.配置墙
# 4.可实现替换传入repl resolve(composite_list,repl={'?': 0, '!': 1})

f = []

class Solution:

    def set_maze(self, board, start, target):
        # 将 start, target 在 board 中 重置为可移动
        board[start[0]][start[1]] = UNMOVABLE + 1
        board[target[0]][target[1]] = UNMOVABLE + 1

    def exist(self, board: List[List], target: [int, int], start: [int, int] = [0, 0]) -> bool:
        self.set_maze(board, start, target)
        x, y = target
        sx, sy = start
        row = len(board)
        col = len(board[0])
        lst = []

        def search(i, j, cur):
            if i >= row or i < 0:
                return False
            if j >= col or j < 0:
                return False
            letter = board[i][j]

            if letter == UNMOVABLE or letter == None:
                return False
            if i == x and j == y:  # 找到最后一个
                lst.append([i, j])
                print(lst)
                f.extend(lst)
                return True

            lst.append([i, j])
            board[i][j] = None

            ret = search(i + 1, j, cur + 1) or \
                  search(i - 1, j, cur + 1) or \
                  search(i, j + 1, cur + 1) or \
                  search(i, j - 1, cur + 1)
            # 取消标记 回撤一步
            board[i][j] = letter
            lst.pop()
            return ret

        for i in range(row):
            for j in range(col):
                ret = search(i + sx, j + sy, 0)
                if ret:
                    return True
        print(lst)
        return False


def get_direction(board: List[List[int]]):
    res = ''
    for i in range(len(board) - 1):
        a, b = board[i], board[i + 1]
        r1, c1 = a
        r2, c2 = b
        if r2 - r1 == 1:
            res += 'q'  # line down
        elif r2 - r1 == -1:
            res += 'm'  # line up
        elif c2 - c1 == 1:
            res += 'y'  # right
        elif c2 - c1 == -1:
            res += 'd'  # left
        else:
            print(f'error {i}, {res}')
            res = ''
    return res


def find_start(maze):
    for i in range(len(maze)):
        if maze[i][0] == '!':
            return [i, 0]


def find_end(maze):
    for i in range(len(maze)):
        if maze[i][len(maze) - 1] == '!':
            return [i, len(maze) - 1]


def resolve(maze, target=None, start=None, repl=None):
    global f
    f = []

    if not target:
        target = find_end(maze)
    if not start:
        start = find_start(maze)
    if repl:
        maze = [list(map(lambda k: repl[k], row)) for row in maze]

    a = Solution()
    a.exist(maze, target, start)
    # print(f)
    direction = get_direction(f)
    print(direction)
    return direction


